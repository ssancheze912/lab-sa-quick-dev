describe('AC3 — Deep linking to /clientes and /contactos (FR30)', () => {
  beforeEach(() => {
    setViewportWidth(1280);
  });

  afterEach(() => {
    setViewportWidth(1024);
  });

  it('should render ClientesView when navigating directly to /clientes', async () => {
    // GIVEN: The user types /clientes directly in the browser URL bar
    // WHEN: The route loads
    await renderAppAtRoute('/clientes');

    // THEN: The Clientes view is rendered without any redirect
    expect(screen.getByTestId('clientes-view')).toBeDefined();
  });

  it('should render ContactosView when navigating directly to /contactos', async () => {
    // GIVEN: The user types /contactos directly in the browser URL bar
    // WHEN: The route loads
    await renderAppAtRoute('/contactos');

    // THEN: The Contactos view is rendered without any redirect
    expect(screen.getByTestId('contactos-view')).toBeDefined();
  });

  it('should highlight the Clientes nav item as active when at /clientes', async () => {
    // GIVEN: The user navigates directly to /clientes
    // WHEN: The page renders
    await renderAppAtRoute('/clientes');

    // THEN: The Clientes nav item has the active state attribute
    const clientesItem = screen.getByTestId('nav-item-clientes');
    expect(clientesItem.getAttribute('data-active')).toBe('true');
  });

  it('should highlight the Contactos nav item as active when at /contactos', async () => {
    // GIVEN: The user navigates directly to /contactos
    // WHEN: The page renders
    await renderAppAtRoute('/contactos');

    // THEN: The Contactos nav item has the active state attribute
    const contactosItem = screen.getByTestId('nav-item-contactos');
    expect(contactosItem.getAttribute('data-active')).toBe('true');
  });

  it('should redirect root path / to /clientes', async () => {
    // GIVEN: The user navigates to the root path /
    // WHEN: The route resolves
    const { routeTree } = await import('../../routeTree.gen');
    const memoryHistory = createMemoryHistory({ initialEntries: ['/'] });
    const router = createRouter({ routeTree, history: memoryHistory });

    render(<RouterProvider router={router} />);
    await router.load();

    // THEN: The URL resolves to /clientes and the Clientes view is shown
    expect(router.state.location.pathname).toBe('/clientes');
    expect(screen.getByTestId('clientes-view')).toBeDefined();
  });

  it('should not redirect away from /clientes when loading it directly', async () => {
    // GIVEN: The user navigates directly to /clientes
    // WHEN: The router resolves the route
    const { routeTree } = await import('../../routeTree.gen');
    const memoryHistory = createMemoryHistory({ initialEntries: ['/clientes'] });
    const router = createRouter({ routeTree, history: memoryHistory });

    render(<RouterProvider router={router} />);
    await router.load();

    // THEN: The URL remains /clientes — no unexpected redirect
    expect(router.state.location.pathname).toBe('/clientes');
  });

  it('should not redirect away from /contactos when loading it directly', async () => {
    // GIVEN: The user navigates directly to /contactos
    // WHEN: The router resolves the route
    const { routeTree } = await import('../../routeTree.gen');
    const memoryHistory = createMemoryHistory({ initialEntries: ['/contactos'] });
    const router = createRouter({ routeTree, history: memoryHistory });

    render(<RouterProvider router={router} />);
    await router.load();

    // THEN: The URL remains /contactos — no unexpected redirect
    expect(router.state.location.pathname).toBe('/contactos');
  });
});

