describe('AC4 — 404 not-found view for unknown routes', () => {
  beforeEach(() => {
    setViewportWidth(1280);
  });

  afterEach(() => {
    setViewportWidth(1024);
  });

  it('should render the not-found view when navigating to an unknown route', async () => {
    // GIVEN: The user navigates to an unknown route (e.g., /unknown-path)
    // WHEN: The page loads
    await renderAppAtRoute('/unknown-path');

    // THEN: The not-found view is displayed
    expect(screen.getByTestId('not-found-view')).toBeDefined();
    expect(screen.getByTestId('not-found-view')).toBeVisible();
  });

  it('should display a Spanish message on the 404 view', async () => {
    // GIVEN: The user navigates to an unknown route
    // WHEN: The not-found component renders
    await renderAppAtRoute('/unknown-path');

    // THEN: A message containing "no encontrada" (Spanish) is displayed
    const message = screen.getByTestId('not-found-message');
    expect(message).toBeDefined();
    expect(message).toHaveTextContent('no encontrada');
  });

  it('should display a "Volver a Clientes" link pointing to /clientes on the 404 view', async () => {
    // GIVEN: The user navigates to an unknown route
    // WHEN: The not-found component renders
    await renderAppAtRoute('/unknown-path');

    // THEN: A link back to /clientes is visible with the correct href
    const backLink = screen.getByTestId('not-found-back-link');
    expect(backLink).toBeDefined();
    expect(backLink).toHaveAttribute('href', '/clientes');
  });

  it('should navigate to /clientes when clicking the return link on 404 view', async () => {
    // GIVEN: The user is on the not-found view
    const { routeTree } = await import('../../routeTree.gen');
    const memoryHistory = createMemoryHistory({ initialEntries: ['/unknown-path'] });
    const router = createRouter({ routeTree, history: memoryHistory });

    render(<RouterProvider router={router} />);
    await router.load();

    // WHEN: The user clicks the "Volver a Clientes" link
    await act(async () => {
      fireEvent.click(screen.getByTestId('not-found-back-link'));
      await router.load();
    });

    // THEN: The user is navigated to /clientes
    expect(router.state.location.pathname).toBe('/clientes');
    expect(screen.getByTestId('clientes-view')).toBeDefined();
  });
});

