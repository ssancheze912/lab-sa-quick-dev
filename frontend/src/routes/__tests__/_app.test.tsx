/**
 * Sets window.innerWidth to simulate a specific viewport width.
 * Required because jsdom does not resize on its own.
 */
function setViewportWidth(width: number): void {
  Object.defineProperty(window, 'innerWidth', {
    writable: true,
    configurable: true,
    value: width,
  });
  window.dispatchEvent(new Event('resize'));
}

/**
 * Renders the application shell at a given route using an in-memory router.
 * Import the routeTree once it is generated by TanStack Router plugin.
 *
 * NOTE: routeTree import will fail (RED) until the implementation files are created
 * and `pnpm run dev` regenerates routeTree.gen.ts.
 */
async function renderAppAtRoute(path: string) {
  // Dynamic import — will throw ModuleNotFoundError in RED phase
  // (routeTree.gen.ts does not exist until _app.tsx and its children are created)
  const { routeTree } = await import('../../routeTree.gen');

  const memoryHistory = createMemoryHistory({ initialEntries: [path] });
  const router = createRouter({ routeTree, history: memoryHistory });

  render(<RouterProvider router={router} />);

  // Wait for async router transitions to settle
  await router.load();
}

// ─────────────────────────────────────────────────────────────────────────────
// AC1 — Desktop NavigationRail (viewport >= 1024px)
// ─────────────────────────────────────────────────────────────────────────────

describe('AC1 — Desktop NavigationRail (viewport >= 1024px)', () => {
  beforeEach(() => {
    setViewportWidth(1280);
  });

  afterEach(() => {
    setViewportWidth(1024);
  });

  it('should render the NavigationRail on desktop viewport', async () => {
    // GIVEN: The application is loaded on a desktop browser (viewport >= 1024px)
    // WHEN: The component tree is rendered at /clientes
    await renderAppAtRoute('/clientes');

    // THEN: The NavigationRail component is present in the DOM
    expect(screen.getByTestId('navigation-rail')).toBeDefined();
  });

  it('should show a "Clientes" entry inside the NavigationRail on desktop', async () => {
    // GIVEN: The application shell is rendered on desktop
    // WHEN: The layout renders at /clientes
    await renderAppAtRoute('/clientes');

    // THEN: A "Clientes" nav item is visible
    expect(screen.getByTestId('nav-item-clientes')).toBeDefined();
    expect(screen.getByTestId('nav-item-clientes')).toHaveTextContent('Clientes');
  });

  it('should show a "Contactos" entry inside the NavigationRail on desktop', async () => {
    // GIVEN: The application shell is rendered on desktop
    // WHEN: The layout renders at /clientes
    await renderAppAtRoute('/clientes');

    // THEN: A "Contactos" nav item is visible
    expect(screen.getByTestId('nav-item-contactos')).toBeDefined();
    expect(screen.getByTestId('nav-item-contactos')).toHaveTextContent('Contactos');
  });

  it('should NOT render the NavigationBar on desktop viewport', async () => {
    // GIVEN: The application is loaded on desktop (viewport >= 1024px)
    // WHEN: The layout renders
    await renderAppAtRoute('/clientes');

    // THEN: The NavigationBar is not visible (hidden via TailwindCSS responsive class)
    const navBar = screen.queryByTestId('navigation-bar');
    // Either not in the DOM or hidden — the element may exist but must not be displayed
    if (navBar) {
      expect(navBar).not.toBeVisible();
    } else {
      expect(navBar).toBeNull();
    }
  });

  it('should navigate to /contactos without full page reload when clicking Contactos', async () => {
    // GIVEN: The application is on /clientes on desktop
    // WHEN: The user clicks the "Contactos" nav item
    await renderAppAtRoute('/clientes');

    await act(async () => {
      fireEvent.click(screen.getByTestId('nav-item-contactos'));
    });

    // THEN: The Contactos view is rendered (SPA navigation — no full reload)
    expect(screen.getByTestId('contactos-view')).toBeDefined();
  });

  it('should navigate to /clientes without full page reload when clicking Clientes', async () => {
    // GIVEN: The application is on /contactos on desktop
    // WHEN: The user clicks the "Clientes" nav item
    await renderAppAtRoute('/contactos');

    await act(async () => {
      fireEvent.click(screen.getByTestId('nav-item-clientes'));
    });

    // THEN: The Clientes view is rendered (SPA navigation — no full reload)
    expect(screen.getByTestId('clientes-view')).toBeDefined();
  });
});

